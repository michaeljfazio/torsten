    /// Helper: load credentials from a raw KES key slice.
    ///
    /// Returns the loaded `BlockProducerCredentials` so callers can inspect
    /// the full credential state (not just vrf_skey as in the VRF helpers above).
    fn load_creds_with_kes(raw_kes: &[u8]) -> anyhow::Result<BlockProducerCredentials> {
        let vrf_kp = torsten_crypto::vrf::generate_vrf_keypair();
        let cold_sk = torsten_crypto::keys::PaymentSigningKey::generate();
        let cold_vk = cold_sk.verification_key().to_bytes();

        // Derive the KES public key from a fresh keypair so the opcert can
        // reference a legitimate 32-byte hot key.
        let seed = [0x11u8; 32];
        let (_kes_sk, kes_pk_bytes) = torsten_crypto::kes::kes_keygen(&seed).unwrap();
        let kes_vkey: [u8; 32] = kes_pk_bytes[..32].try_into().unwrap();

        let (vrf_file, _) = make_vrf_skey_json(vrf_kp.secret_key());
        let kes_file = make_kes_skey_json(raw_kes);
        let opcert_file = make_opcert_json(&kes_vkey, &cold_vk);

        BlockProducerCredentials::load(vrf_file.path(), kes_file.path(), opcert_file.path())
    }

    /// cardano-cli writes 608 bytes of raw KES key material to the text envelope.
    /// Loading such a file must succeed and produce a 612-byte internal buffer
    /// with the period counter initialised to zero.
    #[test]
    fn test_load_kes_skey_608_bytes_appends_period_counter() {
        let seed = [0x22u8; 32];
        let (kes_sk_612, _) = torsten_crypto::kes::kes_keygen(&seed).unwrap();
        assert_eq!(
            kes_sk_612.len(),
            torsten_crypto::kes::KES_SECRET_KEY_BUFFER_SIZE
        );

        // Simulate the text-envelope format: strip the 4-byte period counter.
        let kes_sk_608 = &kes_sk_612[..torsten_crypto::kes::KES_SECRET_KEY_SIZE];
        assert_eq!(kes_sk_608.len(), 608);

        let creds =
            load_creds_with_kes(kes_sk_608).expect("608-byte KES key must load successfully");

        // The resulting buffer must be 612 bytes.
        assert_eq!(
            creds.kes_skey.len(),
            torsten_crypto::kes::KES_SECRET_KEY_BUFFER_SIZE,
            "loaded KES skey must be padded to {} bytes",
            torsten_crypto::kes::KES_SECRET_KEY_BUFFER_SIZE,
        );

        // The period counter appended by the loader must be zero (period 0).
        let period =
            torsten_crypto::kes::kes_get_period(&creds.kes_skey).expect("kes_get_period must work");
        assert_eq!(
            period, 0,
            "initial KES period must be 0 after loading from text envelope"
        );
    }

